// package.json

// The manifest file for a Node.js project.
// Created with npm init or npm init -y.

// Contains:

// Project info (name, version, description, author).
// Scripts (like npm start, npm test).
// Dependencies (packages required for the project).
// DevDependencies (packages only needed for development).


// Dependencies

// Stored under "dependencies" in package.json.
// These are required for your app to run in production.

// Example:
// express → required for running the server.
// mongoose → required for connecting to MongoDB.


// DevDependencies

// Stored under "devDependencies".
// Used only during development, not needed in production.

// Examples:
// nodemon → auto-restarts the server during development.
// jest → testing framework.
// eslint → code linting.


// package.json = project’s blueprint.

// "dependencies" = must-have libraries to run your app.

// "devDependencies" = tools to help you during development.