// Internet Cookies

// What they are:
// Small text files that a website stores on your device.

// Purpose:

// Store user-specific info like login status, preferences, cart items, session IDs.
// Used for authentication (stay logged in) and personalization (dark mode, language, etc.).
// Also used in tracking/ads (e.g., remembering what you browsed).
// Size: Usually very small (a few KB).
// Lifespan: Can be session cookies (deleted when you close the browser) or persistent cookies (stay until expiration date).



// Internet Cache

// What it is:
// Temporary storage of website files (images, CSS, JavaScript, videos, etc.) on your device.

// Purpose:

// Makes websites load faster on repeat visits (no need to download the same files again).
// Reduces bandwidth usage.
// Size: Can be large (MBs to GBs).
// Lifespan: Cached files expire after a set time (controlled by server or browser settings).


// Cookies = The waiter remembering your favorite dish.

// Cache = The restaurant keeping pre-cooked ingredients ready so your meal comes faster.