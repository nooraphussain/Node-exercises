
// node.js?

// Node.js is a JavaScript runtime environment that allows you to run JavaScript code outside of a browser.
// Instead of being limited to client-side scripts (like in Chrome, Firefox, etc.), Node.js lets you use JavaScript on the server side.


// 🔑 What Node.js Is

// Built on Google Chrome’s V8 engine (fast JavaScript execution).
// Provides an event-driven, non-blocking I/O model, making it lightweight and efficient.
// Lets developers build scalable server-side applications in JavaScript.

// ⚡ Why Use Node.js?

// High performance for real-time apps (e.g., chat apps, streaming services).
// Uses single-threaded event loop to handle many connections concurrently.
// Huge ecosystem via npm (Node Package Manager) with millions of libraries.

// 📂 Common Uses

// Backend web servers (Express.js is the most popular framework).
// REST APIs and GraphQL APIs
// Real-time applications (chat, games, live notifications).
// Command-line tools.
// Microservices.
// Even desktop apps (with Electron).